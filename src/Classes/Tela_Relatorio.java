    public void tabela(int setor) {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        metodos x = new metodos();
        
        ArrayList<Tipo_Imovel> vetor = new ArrayList<Tipo_Imovel>();
        
        if (setor == 1) {
            vetor = x.alugado();
        }else{
            vetor = x.disponivel();
        }

        for (int i = 0; i < vetor.size(); i++) {
            modelo.addRow(new Object[]{
                vetor.get(i).getTipo(),
                vetor.get(i).getBairro(),
                vetor.get(i).getValor_base(),
                vetor.get(i).getValorfinal()
            });
        }

    }

